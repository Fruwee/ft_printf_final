int hex_len(int i)
{
	int len;

	len = 0;
	while (i >= 1)
	{
		len++;
		i = i / 16;
	}
	return (len - 1);
}

int n_len(int i)
{
	int l;

	l = 1;
	while ((i % 10) >= 1)
	{
		l++;
	}
	return (l);
}
